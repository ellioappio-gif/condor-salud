// ─── Quick lookup helpers ────────────────────────────────────

export function getPAMICommonCodes(): PAMINomencladorEntry[] {
  return PAMI_NOMENCLADOR.filter((e) => e.destacado);
}

export function searchPAMINomenclador(query: string): PAMINomencladorEntry[] {
  const q = query.toLowerCase().trim();
  if (!q) return PAMI_NOMENCLADOR;
  return PAMI_NOMENCLADOR.filter(
    (e) =>
      e.codigo.includes(q) ||
      e.descripcion.toLowerCase().includes(q) ||
      e.capitulo.toLowerCase().includes(q),
  );
}

export function getPAMIByCapitulo(capitulo: string): PAMINomencladorEntry[] {
  if (capitulo === "Todos") return PAMI_NOMENCLADOR;
  return PAMI_NOMENCLADOR.filter((e) => e.capitulo === capitulo);
}

export function getPAMIStats() {
  const requireAuth = PAMI_NOMENCLADOR.filter((e) => e.requiereAutorizacion).length;
  const noAuth = PAMI_NOMENCLADOR.length - requireAuth;
  const capitulos = new Set(PAMI_NOMENCLADOR.map((e) => e.capitulo)).size;
  return {
    total: PAMI_NOMENCLADOR.length,
    requireAuth,
    noAuth,
    capitulos,
    lastUpdate: "01/02/2026",
  };
}
